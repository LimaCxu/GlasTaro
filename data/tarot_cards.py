def get_all_cards():
    """获取所有塔罗牌"""
    all_cards = []
    
    # 添加大阿卡纳
    for card_id, card_info in MAJOR_ARCANA.items():
        all_cards.append({
            'id': f'major_{card_id}',
            'name': card_info['name'],
            'english_name': card_info['english_name'],
            'type': 'major',
            'upright_meaning': card_info['upright_meaning'],
            'reversed_meaning': card_info['reversed_meaning'],
            'description': card_info['description']
        })
    
    # 添加小阿卡纳
    for suit, suit_info in MINOR_ARCANA.items():
        for card_name, card_info in suit_info['cards'].items():
            all_cards.append({
                'id': f'{suit}_{card_name}',
                'name': card_info['name'],
                'type': 'minor',
                'suit': suit_info['suit_name'],
                'element': suit_info['element'],
                'meaning': card_info['meaning'],
                'suit_meaning': suit_info['meaning']
            })
    
    return all_cards

def get_card_by_id(card_id):
    """根据ID获取特定塔罗牌"""
    all_cards = get_all_cards()
    for card in all_cards:
        if card['id'] == card_id:
            return card
    return None